Array.prototype.map = function(callback) {
    var T, A, k;
    if (this == null) {
      throw new TypeError('this is null or not defined');
    }
    var O = Object(this);
    var len = O.length >>> 0;
    if (typeof callback !== 'function') {
      throw new TypeError(callback + ' is not a function');
    }
    if (arguments.length > 1) {
      T = arguments[1];
    }
    A = new Array(len);
    k = 0;
    while (k < len) {
      var kValue, mappedValue;
      if (k in O) {
        kValue = O[k];
        mappedValue = callback.call(T, kValue, k, O);
        let temp = {};
        for (let key in mappedValue){
          if (key === 'id') {
            temp.id = mappedValue.id;
          }
          if (key === 'title') {
            temp.title = mappedValue.title;
          }
        }
        A[k] = temp;
      }
      k++;
    }
    return A;
  };

